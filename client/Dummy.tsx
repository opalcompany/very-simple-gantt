function makeBarsAndRows(startDate: Date) {
  const { rowNum, rows }: { rowNum: number; rows: GanttRow<string>[] } =
    makeRows();

  let bars: GanttBar<GanttData>[] | undefined;
  bars = makeBars(startDate, rowNum);
  return { startDate: startDate, endDate: new Date(2021, 9, 30), rows, bars };
}

function makeRows() {
  const rows: GanttRow<string>[] = [];
  const rowNum = 5 + (Math.random() < 0.5 ? 1 : 0);

  for (let i = 0; i < 1; i++) {
    let r: GanttRow<string> = {
      row: i,
      caption: "Machine " + i,
      borderColor: "#006600",
      color: "#00cc00",
      data: i.toString(),
    };
    rows.push(r);
  }
  return { rowNum, rows };
}

function makeBars(expStart: Date, rowNum: number) {
  const bars: GanttBar<GanttData>[] = [];
  for (let e = 1; e <= 1; e++) {
    let dateLimit = expStart;

    for (let r = 0; r < rowNum; r++) {
      const data = new GanttData();
      data.experimentId = e;
      data.actionId = r + 1;
      data.originalDurationInMillis = MILLIS_IN_DAY * Math.random() * 7;
      let b: GanttBar<GanttData> = {
        row: r,
        startTime: dateLimit,
        endTime: new Date(dateLimit.getTime() + data.originalDurationInMillis),
        height: 44,
        barColor: d3.interpolateRainbow(Math.random()),
        id: "EXP" + e + "AZZ" + r,
        captions: [
          "EX " + e,
          " ACT" +
            r +
            " ACT" +
            r +
            " ACT" +
            r +
            " ACT" +
            r +
            " ACT" +
            r +
            " ACT" +
            r,
        ],
        draggable: data.actionId === 1,
        resizeble: r === 0 || (r + e) % 2 === 0,
        data: data,
        //opacity: 0.5,
        classes: ["test", `step-${r}`, `exp-${e}`],
      };
      bars.push(b);

      dateLimit = b.endTime;
      if (r === 0) expStart = b.endTime;
    }
  }
  return bars;
}
