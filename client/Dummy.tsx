type GanttViewerProps = {
  startDate: Date;
  endDate: Date;
  rows: GanttRow<string>[];
  bars: GanttBar<GanttData>[];
};

const GanttViewer: React.FC<GanttViewerProps> = (props) => {
  var [gantt, setGantt] = useState<Gantt<string, GanttData>>();
  var [startRealtime, setStartRealtime] = useState<Date>(new Date());
  var [nowBarTime, setNowBarTime] = useState<Date>(new Date());

  /* The useRef Hook creates a variable that "holds on" to a value across rendering
       passes. In this case it will hold our component's SVG DOM element. It's
       initialized null and React will assign it later (see the return statement) */
  const d3Container = useRef<HTMLDivElement>(null);

  //const onPixelDuration = useCallback((millis?: number) => {}, [timer]);

  useEffect(() => {
    setStartRealtime(new Date());
  }, []);

  useEffect(() => {
    const t0 = props.bars[0].startTime;

    console.log("E1");
    var gantt = new Gantt<string, GanttData>(d3Container.current!, {
      ...DEFAULT_OPTIONS,
      rowHeight: 60,
      headers: {
        ...DEFAULT_OPTIONS.headers,
        width: 200,
        style: { "font-size": 10 },
      },
      bars: {
        ...DEFAULT_OPTIONS.bars,
        fontSizes: [11, 11],
        roundness: 4,
      },
      timebar: {
        ...DEFAULT_OPTIONS.timebar,
        //tickValues: [new Date(1689760801000)],
        ticks: 3,
        timeFormatter: (d) => d.getTime() / 1000 + " s",
      },
    });

    let timer: NodeJS.Timer | undefined;
    const f = () => {
      console.log("Tick " + new Date());
      setNowBarTime(
        new Date(t0.getTime() + new Date().getTime() - startRealtime.getTime())
      );
    };

    gantt.onPixelDuration((millis) => {
      console.log("clearing " + timer);
      if (timer) clearInterval(timer);
      const i = millis && Math.max(millis / 10, 50);
      console.log("setting " + millis + "=>" + i);
      timer = millis ? setInterval(f, i) : undefined;
    });

    const onEndDrag = (
      bar: GanttBar<GanttData>,
      bars: GanttBar<GanttData>[]
    ): void => {};

    const onPan = (delta: number) => {};

    const onDrag = (
      bar: GanttBar<GanttData>,
      newStartTime: Date,
      bars: GanttBar<GanttData>[]
    ): void => {
      bars = JSON.parse(JSON.stringify(bars), (key, v) =>
        key.endsWith("Time") ? new Date(Date.parse(v)) : v
      ); //make a deep clone
      const delta2 = differenceInSeconds(newStartTime, bar.startTime);
      bars.forEach((b) => {
        b.startTime = addSeconds(b.startTime, delta2);
        b.endTime = addSeconds(b.endTime, delta2);
      });
      gantt.doUpdateBars(bars, false);
      return;
      const draggedBarData = bar.data as GanttData;
      console.log(
        "dragging experiment " +
          draggedBarData.experimentId! +
          " action " +
          draggedBarData.actionId!
      );
      const delta = newStartTime.valueOf() - bar.startTime.valueOf();

      const ok = bars.every((b) => {
        const bd = b.data;
        if (
          bd.experimentId! === draggedBarData.experimentId! &&
          bd.actionId! > draggedBarData.actionId!
        ) {
          const tmpEndTime = new Date(b.endTime.valueOf() + delta);
          return !(tmpEndTime > props.endDate);
        } else {
          return true;
        }
      });

      if (!ok) {
        return;
      }

      bars.forEach((b) => {
        const bd = b.data as GanttData;
        if (
          bd.experimentId! === draggedBarData.experimentId! &&
          bd.actionId! >= draggedBarData.actionId!
        ) {
          b.startTime = new Date(b.startTime.valueOf() + delta);
          b.endTime = new Date(b.endTime.valueOf() + delta);
        }
      });

      gantt.doUpdateBars(bars);
    };

    const onResize = (
      resizedBar: GanttBar<GanttData>,
      newEndTime: Date,
      bars: GanttBar<GanttData>[]
    ): void => {
      bars = JSON.parse(JSON.stringify(bars), (key, v) =>
        key.endsWith("Time") ? new Date(Date.parse(v)) : v
      ); //make a deep clone
      //const newBars = JSON.parse(JSON.stringify(bars), dateTimeReviver) as GanttBar[]

      const resizedBarData = resizedBar.data as GanttData;
      console.log(
        "resizing experiment " +
          resizedBarData.experimentId! +
          " action " +
          resizedBarData.actionId!
      );
      const newDuration = newEndTime.getTime() - resizedBar.startTime.getTime();
      if (
        newDuration > resizedBarData.originalDurationInMillis * 1.4 ||
        newDuration < resizedBarData.originalDurationInMillis * 0.6
      ) {
        return;
      } else {
        //resizedBar.endTime = newEndTime
        var t = newEndTime;
        bars
          .sort((b1, b2) => b1.data.actionId! - b2.data.actionId!)
          .forEach((b) => {
            const bd = b.data as GanttData;
            if (bd.experimentId! !== resizedBarData.experimentId!) return;
            if (bd.actionId! > resizedBarData.actionId!) {
              const barDuration = b.endTime.getTime() - b.startTime.getTime();
              b.startTime = t;
              t = new Date(t.getTime() + barDuration);
              b.endTime = t;
            } else if (bd.actionId! === resizedBarData.actionId!) {
              b.endTime = newEndTime;
            }
          });
        gantt.doUpdateBars(bars);
      }
    };

    gantt.onEndDrag = onEndDrag;
    gantt.onDrag = onDrag;
    gantt.onResize = onResize;
    gantt.pan = {
      onPan: onPan,
      mouseModifier: [
        { ctrlKey: true, altKey: true, metaKey: true, shiftKey: true },
      ],
    };

    gantt.onTooltip = (bar) =>
      ReactDOM.render(<div>ciao id: {bar.id}</div>, gantt.tooltipNode);
    setGantt(gantt);
    f();
    return () => {
      clearInterval(timer);
    };
  }, []); //ony once

  useEffect(() => {
    console.log("E2");
    gantt?.setTimeRange(props.startDate, props.endDate);
    gantt?.reload(props.rows, props.bars);
    console.log(props.bars[0]);
  }, [props, gantt]);

  useEffect(() => {
    gantt?.setDecorations([
      { type: "line", time: nowBarTime, class: "line1" },
      {
        type: "line",
        time: new Date(props.bars[0].startTime.getTime() + 1000 * 3600 * 6),
        class: "line2",
      },
    ]);
  }, [nowBarTime]);

  return (
    <>
      <div className="very-simple-gantt" ref={d3Container} />
    </>
  );
};

